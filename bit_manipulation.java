public class bit_manipulation{
    public static void main(String[] args){
    // Bit Manipulation.
     
    //  1. Bitwise AND
    System.out.println( "Bitswise AND Operator : " + (5&6));

    // 2. Bitwise OR
    System.out.println( "Bitwise OR Operator : " +(5|6) );

    // 3. Bitwise XOR
    System.out.println( "Bitwise XOR Operator : " +(5^6));

    // 4. Binary One's Complement
    System.out.println( "Binary One's Complement : " +(~5));

    // 5. Binary Right Shift
    System.out.println(" Binary Right Shift : " +(5>>2));

    //6. Binary Left Shift 
    System.out.println("Binary Left Shift : " + (5<<2));
    }
}